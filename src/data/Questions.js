    static createWrongAnswers(correctAnswer, questionType = 'general') {
        const wrongAnswers = [];
        
        // Generate context-appropriate wrong answers
        if (questionType === 'math' || !isNaN(parseFloat(correctAnswer))) {
            const num = parseFloat(correctAnswer);
            if (!isNaN(num)) {
                wrongAnswers.push((num + 1).toString());
                wrongAnswers.push((num - 1).toString());
                wrongAnswers.push((num * 2).toString());
                return wrongAnswers.slice(0, 3);
            }
        }

        // For non-numeric answers, create plausible alternatives
        const commonWrongAnswers = {
            'Paris': ['London', 'Berlin', 'Madrid'],
            'Water': ['Oxygen', 'Hydrogen', 'Air'],
            'Mars': ['Venus', 'Jupiter', 'Mercury'],
            'Shakespeare': ['Dickens', 'Milton', 'Chaucer'],
            '8': ['6', '10', '12'],
            'Pacific Ocean': ['Atlantic Ocean', 'Indian Ocean', 'Arctic Ocean'],
            'Carbon Dioxide': ['Oxygen', 'Nitrogen', 'Hydrogen'],
            '1945': ['1944', '1946', '1943'],
            'Diamond': ['Gold', 'Iron', 'Carbon'],
            'Mount Everest': ['K2', 'Kilimanjaro', 'McKinley']
        };

        if (commonWrongAnswers[correctAnswer]) {
            return commonWrongAnswers[correctAnswer];
        }

        // Generic wrong answers if no specific ones are found
        return ['Option A', 'Option B', 'Option C'];
    }

    static validateQuestion(question) {
        return question && 
               question.question && 
               question.answer && 
               typeof question.question === 'string' &&
               typeof question.answer === 'string' &&
               question.question.length > 0 &&
               question.answer.length > 0;
    }

    static getFormattedQuestion(questionData) {
        if (!this.validateQuestion(questionData)) {
            return null;
        }

        return {
            id: questionData.id || Math.random().toString(36).substr(2, 9),
            question: questionData.question,
            answer: questionData.answer,
            level: questionData.level || 1,
            type: questionData.type || 'general',
            category: questionData.category || 'general',
            wrongAnswers: this.createWrongAnswers(questionData.answer, questionData.type)
        };
    }
